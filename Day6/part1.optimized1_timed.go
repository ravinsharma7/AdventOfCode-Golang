// Position and direction handling
type Position struct {
	X, Y int
}

type Direction int

const (
	Up Direction = iota
	Right
	Down
	Left
)

var directions = []Position{
	{-1, 0}, // Up
	{0, 1},  // Right
	{1, 0},  // Down
	{0, -1}, // Left
}

var nextDirection = map[Direction]Direction{
	Up:    Right,
	Right: Down,
	Down:  Left,
	Left:  Up,
}

// Function to parse the input map
func parseMap(input string) ([][]rune, Position, Direction) {
	lines := strings.Split(strings.TrimSpace(input), "\n")
	grid := make([][]rune, len(lines))
	var guardPos Position
	var guardDir Direction

	for i, line := range lines {
		grid[i] = []rune(line)
		for j, char := range line {
			switch char {
			case '^':
				guardPos = Position{i, j}
				guardDir = Up
			case '>':
				guardPos = Position{i, j}
				guardDir = Right
			case 'v':
				guardPos = Position{i, j}
				guardDir = Down
			case '<':
				guardPos = Position{i, j}
				guardDir = Left
			}
		}
	}
	return grid, guardPos, guardDir
}

// Function to simulate the guard's patrol
func simulatePatrol(grid [][]rune, guardPos Position, guardDir Direction) int {
	rows := len(grid)
	cols := len(grid[0])
	visited := make([][]bool, rows)
	for i := range visited {
		visited[i] = make([]bool, cols)
	}

	distinctCount := 0

	for {
		// Mark current position as visited
		if !visited[guardPos.X][guardPos.Y] {
			visited[guardPos.X][guardPos.Y] = true
			distinctCount++
		}

		// Compute next position
		nextPos := Position{
			X: guardPos.X + directions[guardDir].X,
			Y: guardPos.Y + directions[guardDir].Y,
		}

		// Check bounds and obstacles
		if nextPos.X < 0 || nextPos.X >= rows || nextPos.Y < 0 || nextPos.Y >= cols {
			// Guard leaves the map
			break
		}
		if grid[nextPos.X][nextPos.Y] == '#' {
			// Turn right if obstacle encountered
			guardDir = nextDirection[guardDir]
		} else {
			// Move forward
			guardPos = nextPos
		}
	}

	return distinctCount
}

